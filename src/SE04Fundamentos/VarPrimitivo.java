package SE04Fundamentos;

public class VarPrimitivo {

	public static void main(String[] args) {
		
		// var reconoce el tipo segun el dato ingresado
		var numeroPrueba = 10.2;
		
		//
		
		System.out.println(numeroPrueba);
		
		// Char
		// unicode
		char carcter = 'a';
		char mander = '\u0021';
		char decm = 33;
		char simb = '!';
		
		var pr = '\u0022';
		
		System.out.println(carcter);
		System.out.println(mander);
		System.out.println(decm);
		System.out.println(simb);
		
		
		System.out.println(pr);
		
		int unicode = '"';
		
		System.out.println(unicode);
	
	}
}
